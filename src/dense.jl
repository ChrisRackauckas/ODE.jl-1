function onestep!{O<:ExplicitODE,S<:DenseStepper}(s::Solver{O,S}, state::DenseState)
    i = state.tout_i
    if i > length(s.stepper.options.tspan)
        return finish
    end

    # our next output time
    tout = s.stepper.options.tspan[i]

    sol = s.stepper.solver # this looks weird
    sol_state = state.solver_state

    # try to get a new set of steps enclosing `tout`, if all goes
    # right we end up with t∈[t1,t2] with
    # t1,_=output(state.step_prev)
    # t2,_=output(state.solver_state)
    status = next_interval!(sol,sol_state,state.step_prev,tout)
    if status == abort
        # we failed to get enough steps
        warn("Iterator was exhausted before the dense output could produce the output.")
        return abort
    else
        # we got the steps, proceed with the interpolation, this fills
        # the state.step_out with y(tout) and y'(tout) according to an
        # interpolation algorithm specific for a method (defaults to
        # hermite O(3)).
        interpolate!(state.solver_state,state.step_prev,tout,state.step_out)

        # increase the counter
        state.tout_i += 1
        return cont
    end
end

"""

Pulls the results from the (solver,state) pair using `onestep!` until
we reach a first step such that `t>=tout`.  It fills the `steps`
variable with (Step(t1,y(t1),dy(t1)),Step(t2,y(t2),dy(t2))), where
`t1` is is the step before `tout` and `t2` is `>=tout`.  In
other words `tout∈[t1,t2]`.

TODO: tdir

"""
function next_interval!(solver,state,step_prev,tout)

    while true
        # get the current time
        t1   = step_prev.t
        t2,_ = output(state)
        t1, t2 = sort([t1,t2])
        if t1 <= tout <= t2
            # we found the enclosing times
            return cont
        end

        # save the current state of solution
        t, y, dy = output(state)
        step_prev.t = t
        copy!(step_prev.y,y)
        copy!(step_prev.dy,dy)

        # try to perform a single step with the solver
        status = onestep!(solver, state)

        if status != cont
            return status
        end
    end

    # this will never happen
    return abort
end

"""

Make dense output using Hermite interpolation of order O(3). Updates
yout in-place.  Only needs y and dy at t1 and t2.
Input
- state::AbstractState -- state of a stepper at time t2
- step_prev::Step -- solution at time t1 respectively
- tout -- time of requested output
- yout -- inplace y output
Ref: Hairer & Wanner p.190

TODO: tdir (I think this works for any order of t1 and t2 but needs
verifying.

TODO: fill dy

TODO: arbitrary order method (change step_prev::Step to step_prevs::Tuple{Step,N})

"""
function interpolate!{T,Y}(state::AbstractState,step_prev::Step{T,Y},tout::T,step_out::Step{T,Y})
    t1,y1,dy1 = output(step_prev)
    t2,y2,dy2 = output(state)
    if tout==t1
        copy!(step_out.y,y1)
    elseif tout==t2
        copy!(step_out.y,y2)
    else
        dt       = t2-t1
        theta    = (tout-t1)/dt
        for i=1:length(y1)
            step_out.y[i] =
                (1-theta)*y1[i] +
                theta*y2[i] +
                theta*(theta-1) *
                ( (1-2*theta)*(y2[i]-y1[i]) +
                  (theta-1)*dt*dy1[i] +
                  theta*dt*dy2[i])
        end
    end
    step_out.t = tout
    return nothing
end
