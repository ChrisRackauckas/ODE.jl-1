# A higher level stepper, defined as a wrapper around another stepper.

"""

Dense output options:

- tspan    ::Vector{T}  output times
- points   ::Symbol which points are returned: `:specified` only the
  ones in tspan or `:all` which includes also the step-points of the solver.
- stopevent  Stop integration at a zero of this function
- roottol    TODO

"""

immutable DenseOptions{T<:Number} <: Options{T}
    tspan    ::Vector{T}
    tstop    ::T
    # points   ::Symbol
    # stopevent::S
    # roottol  ::T
end

@compat function (::Type{DenseOptions{T}}){T}(;
                                              tstop         = T(Inf),
                                              tspan::Vector = T[tstop],
                                              # points::Symbol= :all,
                                              # stopevent::S  = (t,y)->false,
                                              # roottol       = eps(T)^T(1//3),
                                              kargs...)
    DenseOptions{T}(tspan,tstop)
end


"""

A stepper specialized in dense output.  It wraps around another
`Solver` and stores the subsequent steps generated by `Solver` and
interpolates the results on request (currently this means at the
output times stored in `options.tspan`).

"""
immutable DenseStepper{S<:Solver,O<:DenseOptions} <: AbstractStepper
    solver::S
    options::O
end

function solve{T,S<:DenseStepper}(ode::ExplicitODE{T},
                                  ::Type{S};
                                  method = RKStepperAdaptive{:rk45},
                                  options...)
    sol_orig = Solver(ode,method{T}(; options...))
    dense_options = DenseOptions{T}(; options...)
    dense_stepper = S(sol_orig,dense_options)
    return Solver(ode,dense_stepper)
end

"""

The state of the dense stepper

"""
type DenseState{St<:AbstractState,T,Y} <: AbstractState{T,Y}
    tout_i::Int
    step_prev::Step{T,Y}
    step_out::Step{T,Y}
    solver_state::St
end

output(ds::DenseState) = output(ds.step_out)

function init{O<:ExplicitODE,S<:DenseStepper}(s::Solver{O,S})
    ode = s.stepper.solver.ode
    solver_state = init(s.stepper.solver)
    dy0 = similar(ode.y0)
    ode.F!(ode.t0,ode.y0,dy0)
    step_prev = Step(ode.t0,copy(ode.y0),dy0)
    step_out = Step(ode.t0,similar(ode.y0),similar(ode.y0))
    return DenseState(1,step_prev,step_out,solver_state)
end


